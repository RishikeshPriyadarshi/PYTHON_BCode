#return = statement used to end the funcn
#         and send result back to caller


def add(x,y):
    z = x + y
    return z

def subtract(x,y):
    z = x - y
    return z

def multiply(x,y):
    z = x * y
    return z

def divide(x,y):
    z = x / y
    return z

#invoking the funcns and then we will print the results
print(add(1,2))
print(subtract(1,2))
print(multiply(1,2))
print(divide(1,2))



